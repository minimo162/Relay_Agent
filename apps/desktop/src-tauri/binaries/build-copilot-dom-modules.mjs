import fs from "node:fs";
import path from "node:path";

const root = path.dirname(new URL(import.meta.url).pathname);
const serverPath = path.join(root, "copilot_server.js");
const lines = fs.readFileSync(serverPath, "utf8").split("\n");

function slice(a, b) {
  return lines.slice(a - 1, b).join("\n");
}

function toExportConst(block) {
  return block
    .replace(/^var (COMPOSER_ANCESTOR_CLOSEST)/m, "export const $1")
    .replace(/^var (STREAMING_STOP_SELECTORS)/m, "export const $1")
    .replace(/^var (ASSISTANT_REPLY_DOM_SELECTORS)/m, "export const $1")
    .replace(/^var (COPILOT_STREAMING_PLACEHOLDER_LAST_LINES)/m, "export const $1")
    .replace(/^var (RESPONSE_TIMEOUT_MS)/m, "export const $1")
    .replace(/^var (RESPONSE_POLL_INTERVAL_MS)/m, "export const $1")
    .replace(/^var (RESPONSE_PHANTOM_GENERATING_POLLS)/m, "export const $1")
    .replace(/^var (RESPONSE_POST_STABLE_MS)/m, "export const $1")
    .replace(/^var (CHATHUB_ASSISTANT_MIN_CHARS)/m, "export const $1");
}

