#!/usr/bin/env node
import { createHash } from "node:crypto";
import {
  chmodSync,
  copyFileSync,
  existsSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { spawnSync } from "node:child_process";

const root = resolve(import.meta.dirname, "../..");
const rid = readArg("--rid") ?? process.env.RELAY_TARGET_RID ?? platformRid();
const manifestPath = resolve(root, "tools/codex-app-server/manifest.json");
const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const platform = manifest.platforms?.[rid];

if (!platform) {
  throw new Error(`unsupported Codex app-server RID: ${rid}`);
}

const outputRoot = resolve(root, "tools/codex-app-server", rid);
const executablePath = join(outputRoot, platform.executable);

if (existsSync(executablePath) && existsSync(join(outputRoot, "relay-codex-app-server-manifest.json"))) {
  console.log(`fetch-codex-app-server: ${rid} already materialized at ${relativePath(outputRoot)}`);
  process.exit(0);
}

const temp = mkdtempSync(join(tmpdir(), `relay-codex-app-server-${rid}-`));
try {
  const archive = join(temp, "codex-platform.tgz");
  console.log(`fetch-codex-app-server: downloading ${platform.npmSpec}`);
  await download(platform.tarball, archive);
  verifyArchive(archive, platform);
  run("tar", ["-xzf", archive, "-C", temp]);

  rmSync(outputRoot, { recursive: true, force: true });
  mkdirSync(outputRoot, { recursive: true });

  const packageRoot = join(temp, "package");
  const executableSource = join(packageRoot, platform.expectedExecutableSource);
  if (!existsSync(executableSource)) {
    throw new Error(`Codex executable not found in package: ${platform.expectedExecutableSource}`);
  }
  copyFile(executableSource, executablePath);
  makeExecutable(executablePath);

  for (const extra of platform.additionalExecutables ?? []) {
    const extraSource = join(packageRoot, extra);
    if (existsSync(extraSource)) {
      const extraDestination = join(outputRoot, basenameFromManifestPath(extra));
      copyFile(extraSource, extraDestination);
      makeExecutable(extraDestination);
    }
  }

  const bundledRipgrepSource = platform.bundledRipgrepSource
    ? join(packageRoot, platform.bundledRipgrepSource)
    : null;
  if (bundledRipgrepSource && existsSync(bundledRipgrepSource)) {
    const rgName = rid.startsWith("win") ? "rg.exe" : "rg";
    const rgDestination = join(outputRoot, "path", rgName);
    copyFile(bundledRipgrepSource, rgDestination);
    makeExecutable(rgDestination);
  }

  const resourcesSource = join(packageRoot, "vendor", platform.vendorTriple, "codex-resources");
  if (existsSync(resourcesSource)) {
    run("cp", ["-R", resourcesSource, join(outputRoot, "codex-resources")]);
  }

  writeFileSync(
    join(outputRoot, "NOTICE.txt"),
    [
      "Relay Agent bundles the Codex app-server runtime from the pinned npm artifact below.",
      "",
      `Package: ${platform.npmSpec}`,
      `Source: ${manifest.source.repository}`,
      `License: ${manifest.source.license}`,
      `Tarball: ${platform.tarball}`,
      `Integrity: ${platform.integrity}`,
      `shasum: ${platform.shasum}`,
      "",
      "This file is generated by scripts/release/fetch-codex-app-server.mjs.",
      "Do not edit the materialized runtime manually; update tools/codex-app-server/manifest.json instead.",
      "",
    ].join("\n"),
  );

  writeFileSync(
    join(outputRoot, "relay-codex-app-server-manifest.json"),
    JSON.stringify({
      schemaVersion: "RelayMaterializedCodexAppServer.v1",
      rid,
      materializedAt: new Date().toISOString(),
      source: manifest.source,
      platform,
      files: {
        executable: platform.executable,
        notice: "NOTICE.txt",
        bundledRipgrep: platform.bundledRipgrepSource ? `path/${rid.startsWith("win") ? "rg.exe" : "rg"}` : null,
      },
    }, null, 2),
  );

  console.log(`fetch-codex-app-server: wrote ${relativePath(outputRoot)}`);
} finally {
  rmSync(temp, { recursive: true, force: true });
}

function readArg(name) {
  const index = process.argv.indexOf(name);
  return index >= 0 ? process.argv[index + 1] : undefined;
}

function platformRid() {
  if (process.platform === "win32") return "win-x64";
  if (process.platform === "linux") return "linux-x64";
  throw new Error(`unsupported platform: ${process.platform}`);
}

async function download(url, destination) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`download failed ${response.status} ${response.statusText}: ${url}`);
  }
  const bytes = Buffer.from(await response.arrayBuffer());
  writeFileSync(destination, bytes);
}

function verifyArchive(path, expected) {
  const bytes = readFileSync(path);
  const sha512 = createHash("sha512").update(bytes).digest("base64");
  const integrity = `sha512-${sha512}`;
  if (integrity !== expected.integrity) {
    throw new Error(`integrity mismatch for ${expected.npmSpec}: expected ${expected.integrity}, got ${integrity}`);
  }
  const shasum = createHash("sha1").update(bytes).digest("hex");
  if (shasum !== expected.shasum) {
    throw new Error(`shasum mismatch for ${expected.npmSpec}: expected ${expected.shasum}, got ${shasum}`);
  }
  const sha256 = createHash("sha256").update(bytes).digest("hex");
  if (sha256 !== expected.sha256) {
    throw new Error(`sha256 mismatch for ${expected.npmSpec}: expected ${expected.sha256}, got ${sha256}`);
  }
}

function copyFile(source, destination) {
  mkdirSync(dirname(destination), { recursive: true });
  copyFileSync(source, destination);
}

function makeExecutable(path) {
  if (!process.platform.startsWith("win")) {
    chmodSync(path, 0o755);
  }
}

function run(command, args) {
  const result = spawnSync(command, args, { cwd: root, stdio: "inherit" });
  if (result.status !== 0) {
    throw new Error(`${command} ${args.join(" ")} failed with ${result.status}`);
  }
}

function basenameFromManifestPath(path) {
  return path.split(/[\\/]/).at(-1);
}

function relativePath(path) {
  return path.replace(`${root}${process.platform === "win32" ? "\\" : "/"}`, "");
}
